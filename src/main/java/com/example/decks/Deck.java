package com.example.decks;

public class Deck {
	long deck_id;
	String deck_name;
	
	public Deck() {
		
	}

	public long getDeck_id() {
		return deck_id;
	}

	public void setDeck_id(long deck_id) {
		this.deck_id = deck_id;
	}

	public String getDeck_name() {
		return deck_name;
	}

	public void setDeck_name(String deck_name) {
		this.deck_name = deck_name;
	}
	
	
}
